#include "libft.h"

char	*ft_strrchr(char *str, int chr)
{
	int	i;
	int	j;

	i = 0;
	while (str[i])
	{
		j = 0;
		if (str[i] == chr)
	j++;
	i++;
	}
	return (0);
}
