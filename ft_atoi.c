int	ft_atoi(const char *str)
{
	int	sign;
	int	i;
	int	out;

	sign = 1;
	i = 0;
	out = 0;
	if (str[i] == '-')
	{
		sign = -1;
	i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		out = ((out * 10) + (str[i] - '0'));
	i++;
	}
	return (out * sign);
}
